    private float convert(float value, String unit1, String unit2) {
        // Definisikan faktor konversi untuk unit yang berbeda
        float byteToKilobyte = 1f / 1024;
        float byteToMegabyte = byteToKilobyte / 1024;
        float byteToGigabyte = byteToMegabyte / 1024;
        float byteToTerabyte = byteToGigabyte / 1024;
        float byteToPetabyte = byteToTerabyte / 1024;

        // Faktor konversi dari Megabyte (MB) ke unit lain
        float megabyteToKilobyte = 1024;
        float megabyteToGigabyte = 1f / 1024;
        float megabyteToTerabyte = megabyteToGigabyte / 1024;
        float megabyteToPetabyte = megabyteToTerabyte / 1024;

        // Faktor konversi dari Gigabyte (GB) ke unit lain
        float gigabyteToKilobyte = megabyteToKilobyte * 1024;
        float gigabyteToMegabyte = 1024;
        float gigabyteToTerabyte = 1f / 1024;
        float gigabyteToPetabyte = gigabyteToTerabyte / 1024;

        // Faktor konversi dari Terabyte (TB) ke unit lain
        float terabyteToKilobyte = gigabyteToKilobyte * 1024;
        float terabyteToMegabyte = gigabyteToMegabyte * 1024;
        float terabyteToGigabyte = gigabyteToTerabyte * 1024;
        float terabyteToPetabyte = 1f / 1024;

        // Faktor konversi dari Petabyte (PB) ke unit lain
        float petabyteToKilobyte = terabyteToKilobyte * 1024;
        float petabyteToMegabyte = terabyteToMegabyte * 1024;
        float petabyteToGigabyte = terabyteToGigabyte * 1024;
        float petabyteToTerabyte = terabyteToPetabyte * 1024;
        float petabyteToByte = byteToPetabyte;

        // Faktor konversi dari Kilobyte (KB) ke unit lain
        float kilobyteToMegabyte = 1f / 1024;
        float kilobyteToGigabyte = kilobyteToMegabyte / 1024;
        float kilobyteToTerabyte = kilobyteToGigabyte / 1024;
        float kilobyteToPetabyte = kilobyteToTerabyte / 1024;

        // Hitung hasil konversi berdasarkan unit yang dipilih
        if (unit1.equals("Byte B") && unit2.equals("Kilobyte KB")) {
            return value * byteToKilobyte;
        } else if (unit1.equals("Byte B") && unit2.equals("Megabyte MB")) {
            return value * byteToMegabyte;
        } else if (unit1.equals("Byte B") && unit2.equals("Gigabyte GB")) {
            return value * byteToGigabyte;
        } else if (unit1.equals("Byte B") && unit2.equals("Terabyte TB")) {
            return value * byteToTerabyte;
        } else if (unit1.equals("Byte B") && unit2.equals("Petabyte PB")) {
            return value * byteToPetabyte;
        } else if (unit1.equals("Kilobyte KB") && unit2.equals("Byte B")) {
            return value / byteToKilobyte;
        } else if (unit1.equals("Kilobyte KB") && unit2.equals("Megabyte MB")) {
            return value * kilobyteToMegabyte;
        } else if (unit1.equals("Kilobyte KB") && unit2.equals("Gigabyte GB")) {
            return value * kilobyteToGigabyte;
        } else if (unit1.equals("Kilobyte KB") && unit2.equals("Terabyte TB")) {
            return value * kilobyteToTerabyte;
        } else if (unit1.equals("Kilobyte KB") && unit2.equals("Petabyte PB")) {
            return value * kilobyteToPetabyte;
        } else if (unit1.equals("Megabyte MB") && unit2.equals("Kilobyte KB")) {
            return value / kilobyteToMegabyte;
        } else if (unit1.equals("Megabyte MB") && unit2.equals("Gigabyte GB")) {
            return value * megabyteToGigabyte;
        } else if (unit1.equals("Megabyte MB") && unit2.equals("Terabyte TB")) {
            return value * megabyteToTerabyte;
        } else if (unit1.equals("Megabyte MB") && unit2.equals("Petabyte PB")) {
            return value * megabyteToPetabyte;
        } else if (unit1.equals("Gigabyte GB") && unit2.equals("Kilobyte KB")) {
            return value / kilobyteToGigabyte;
        } else if (unit1.equals("Gigabyte GB") && unit2.equals("Megabyte MB")) {
            return value / megabyteToGigabyte;
        } else if (unit1.equals("Gigabyte GB") && unit2.equals("Terabyte TB")) {
            return value * gigabyteToTerabyte;
        } else if (unit1.equals("Gigabyte GB") && unit2.equals("Petabyte PB")) {
            return value * gigabyteToPetabyte;
        } else if (unit1.equals("Terabyte TB") && unit2.equals("Kilobyte KB")) {
            return value / kilobyteToTerabyte;
        } else if (unit1.equals("Terabyte TB") && unit2.equals("Megabyte MB")) {
            return value / megabyteToTerabyte;
        } else if (unit1.equals("Terabyte TB") && unit2.equals("Gigabyte GB")) {
            return value / gigabyteToTerabyte;
        } else if (unit1.equals("Terabyte TB") && unit2.equals("Petabyte PB")) {
            return value * terabyteToPetabyte;
        } else if (unit1.equals("Petabyte PB") && unit2.equals("Kilobyte KB")) {
            return value / kilobyteToPetabyte;
        } else if (unit1.equals("Petabyte PB") && unit2.equals("Megabyte MB")) {
            return value / megabyteToPetabyte;
        } else if (unit1.equals("Petabyte PB") && unit2.equals("Gigabyte GB")) {
            return value / gigabyteToPetabyte;
        } else if (unit1.equals("Petabyte PB") && unit2.equals("Terabyte TB")) {
            return value / terabyteToPetabyte;
        } else if (unit1.equals("Petabyte PB") && unit2.equals("Byte B")) {
            return value * petabyteToByte;
        }

        return value; // Jika unit tidak berubah, kembalikan nilai asli
    }

