    private void addDigitToInput(String digit) {
        if (isTfData1Focused) {
            String currentText = tfData1.getText();
            if (currentText.equals("0")) {
                tfData1.setText(digit);
            } else {
                tfData1.setText(currentText + digit);
            }
        } else if (isTfData2Focused) {
            String currentText = tfData2.getText();
            if (currentText.equals("0")) {
                tfData2.setText(digit);
            } else {
                tfData2.setText(currentText + digit);
            }
        }
    }

